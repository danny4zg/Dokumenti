package hr.kingict.svis.obrasci.web;

public class CertifikatJezici {
	private Integer idJezik;
	private String nazivJezik;
	
	public CertifikatJezici() {
	}
	
	public Integer getIdJezik() {
		return idJezik;
	}
	public void setIdJezik(Integer idJezik) {
		this.idJezik = idJezik;
	}
	public String getNazivJezik() {
		return nazivJezik;
	}
	public void setNazivJezik(String nazivJezik) {
		this.nazivJezik = nazivJezik;
	}
	
	
}
