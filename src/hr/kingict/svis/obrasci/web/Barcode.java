package hr.kingict.svis.obrasci.web;

public class Barcode {
	private Integer idTube;
	private Integer barkod;
	private Integer status;
	
	public Barcode() {
	}

	public Integer getIdTube() {
		return idTube;
	}
	
	public void setIdTube(Integer idTube) {
		this.idTube = idTube;
	}
	
	public Integer getBarkod() {
		return barkod;
	}
	
	public void setBarkod(Integer barkod) {
		this.barkod = barkod;
	}
	
	public Integer getStatus() {
		return status;
	}
	
	public void setStatus(Integer status) {
		this.status = status;
	}
	
}
