package hr.kingict.svis.obrasci.web;

public class Objekt {
	private Integer idObjekt;
	private String objekt;
	private String vkb;
	private Integer idSubjekt;
	private String subjekt;
	private String oib;
	private String adresa;
	private String mjesto;
	
	public Objekt() {
	}
	
	public Integer getIdObjekt() {
		return idObjekt;
	}
	public void setIdObjekt(Integer idObjekt) {
		this.idObjekt = idObjekt;
	}
	public String getObjekt() {
		return objekt;
	}
	public void setObjekt(String objekt) {
		this.objekt = objekt;
	}
	public String getVkb() {
		return vkb;
	}
	public void setVkb(String vkb) {
		this.vkb = vkb;
	}
	public Integer getIdSubjekt() {
		return idSubjekt;
	}
	public void setIdSubjekt(Integer idSubjekt) {
		this.idSubjekt = idSubjekt;
	}
	public String getSubjekt() {
		return subjekt;
	}
	public void setSubjekt(String subjekt) {
		this.subjekt = subjekt;
	}
	public String getOib() {
		return oib;
	}
	public void setOib(String oib) {
		this.oib = oib;
	}
	public String getAdresa() {
		return adresa;
	}
	public void setAdresa(String adresa) {
		this.adresa = adresa;
	}
	public String getMjesto() {
		return mjesto;
	}
	public void setMjesto(String mjesto) {
		this.mjesto = mjesto;
	}
	
}
