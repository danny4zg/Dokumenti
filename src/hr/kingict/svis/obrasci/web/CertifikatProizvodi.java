package hr.kingict.svis.obrasci.web;

public class CertifikatProizvodi {
	private Integer idProizvod;
	private String kodProizvod;
	private String nazivProizvod;
	
	public CertifikatProizvodi() {
		super();
	}
	
	public Integer getIdProizvod() {
		return idProizvod;
	}
	public void setIdProizvod(Integer idProizvod) {
		this.idProizvod = idProizvod;
	}
	public String getKodProizvod() {
		return kodProizvod;
	}
	public void setKodProizvod(String kodProizvod) {
		this.kodProizvod = kodProizvod;
	}
	public String getNazivProizvod() {
		return nazivProizvod;
	}
	public void setNazivProizvod(String nazivProizvod) {
		this.nazivProizvod = nazivProizvod;
	}
	
	
}
