package hr.kingict.svis.obrasci.web;

public class SubjektBean {
	private Integer idSubjekt;
	private String nazivSubjekt;
	private String oib;
	private String adresaSubjekt;
	private String drzavaSubjekt;
	
	public SubjektBean() {
	
	}

	public Integer getIdSubjekt() {
		return idSubjekt;
	}

	public void setIdSubjekt(Integer idSubjekt) {
		this.idSubjekt = idSubjekt;
	}

	public String getNazivSubjekt() {
		return nazivSubjekt;
	}

	public void setNazivSubjekt(String nazivSubjekt) {
		this.nazivSubjekt = nazivSubjekt;
	}

	public String getOib() {
		return oib;
	}

	public void setOib(String oib) {
		this.oib = oib;
	}

	public String getAdresaSubjekt() {
		return adresaSubjekt;
	}

	public void setAdresaSubjekt(String adresaSubjekt) {
		this.adresaSubjekt = adresaSubjekt;
	}

	public String getDrzavaSubjekt() {
		return drzavaSubjekt;
	}

	public void setDrzavaSubjekt(String drzavaSubjekt) {
		this.drzavaSubjekt = drzavaSubjekt;
	}
	
	
	
}
